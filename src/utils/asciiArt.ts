/** 字体名称 -> 字体对象映射，便于 O(1) 查找 */
const FONT_MAP: Record<string, Font> = Object.fromEntries(
  FONTS.map((f) => [f.name, f])
);

/** 渲染选项 */
export interface RenderOptions {
  /** 字体名称（取自 FONTS[i].name） */
  font?: string;
  /** 字符之间的空格数（默认 1） */
  charSpacing?: number;
  /** 未覆盖字符的占位符（默认 '?'） */
  unknownChar?: string;
  /** 是否将输入文本统一转为大写（默认 true，因字体仅含大写字母） */
  upper?: boolean;
  /** 是否去除每行尾部空白（默认 true，避免行尾多余空格） */
  trimTrailing?: boolean;
}

/**
 * 将单行文本渲染为多行 ASCII Art 横幅。
 *
 * 算法步骤：
 *  1. 可选转大写
 *  2. 逐字符在字体表中查找字符数据（按 \n 切分为行数组）
 *  3. 将每个字符的行数组按行号对齐横向拼接，字符间用 charSpacing 个空格分隔
 *  4. 未找到的字符用 unknownChar 渲染（同字体高度，全空格行）
 *  5. 可选去除每行尾部空白
 *
 * @param input 输入文本（支持多字符，建议单行）
 * @param options 渲染选项
 * @returns 多行 ASCII Art 字符串（行间用 \n 分隔）；输入为空返回空字符串
 */
export function renderText(input: string, options: RenderOptions = {}): string {
  const {
    font: fontName = 'Block',
    charSpacing = 1,
    unknownChar = '?',
    upper = true,
    trimTrailing = true,
  } = options;

  const font = FONT_MAP[fontName] || FONTS[0];
  const { height, chars } = font;

  // 空输入直接返回空字符串，避免渲染出全空行
  if (input === '') return '';

  // 预处理：可选转大写，统一换行符
  const text = upper ? input.toUpperCase() : input;
  const sep = ' '.repeat(Math.max(0, charSpacing));

  // 准备占位符行数据：与字体高度一致的全空格行
  const placeholderLines: string[] = [];
  const unknown = chars[unknownChar];
  if (unknown) {
    placeholderLines.push(...unknown.split('\n'));
  } else {
    const maxLen = Math.max(...Object.values(chars).map((s) => s.split('\n')[0].length));
    for (let i = 0; i < height; i++) placeholderLines.push(' '.repeat(maxLen));
  }

  // 计算每个字符的行数据，确保行数与字体高度一致
  const charBlocks: string[][] = [];
  for (const ch of text) {
    const data = chars[ch];
    if (data) {
      const lines = data.split('\n');
      // 行数不足时用空行补齐（防止字体数据异常导致错位）
      while (lines.length < height) lines.push('');
      charBlocks.push(lines.slice(0, height));
    } else {
      charBlocks.push([...placeholderLines]);
    }
  }

  // 逐行横向拼接所有字符
  const resultLines: string[] = [];
  for (let row = 0; row < height; row++) {
    const rowParts: string[] = [];
    for (let col = 0; col < charBlocks.length; col++) {
      rowParts.push(charBlocks[col][row] ?? '');
    }
    let line = rowParts.join(sep);
    if (trimTrailing) line = line.replace(/\s+$/g, '');
    resultLines.push(line);
  }

  return resultLines.join('\n');
}

/**
 * 统计 ASCII Art 输出的基本信息（用于 UI 展示）。
 * @param art 多行 ASCII Art 字符串
 */
export function computeStats(art: string) {
  const lines = art === '' ? 0 : art.split('\n').length;
  const chars = art.length;
  const maxLineWidth = art === '' ? 0 : Math.max(...art.split('\n').map((l) => l.length));
  return { lines, chars, maxLineWidth };
}
