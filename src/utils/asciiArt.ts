/**
 * ASCII Art 文本横幅生成器
 * 纯原生 TypeScript 零依赖实现，无外部字体文件依赖。
 *
 * 设计思路：
 *  - 内置 3 套字体（Block / Banner / Small），字符集覆盖 A-Z、0-9、空格与常用标点
 *  - 每个字符用 \n 分隔的多行字符串表示，行数即字体高度
 *  - 字符宽度可变，渲染时按实际宽度横向拼接，字符间用 1 个空格分隔
 *  - 未覆盖字符统一用占位符（默认为 '?'）渲染，保证输出不变形
 *
 * 适用场景：
 *  - GitHub README / 项目文档标题横幅
 *  - 终端启动横幅、CLI 签名
 *  - 控制台输出装饰、注释块标题
 */

